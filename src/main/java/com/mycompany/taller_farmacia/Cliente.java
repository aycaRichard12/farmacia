    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            // Obtener el ID del cliente que se desea eliminar
            int idCliente = Integer.parseInt(txtIdCliente.getText().trim());

            // Validar que el ID no esté vacío o sea inválido
            if (txtIdCliente.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, 
                    "Por favor, ingresa el ID del cliente a eliminar.", 
                    "Error", 
                    JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Consulta SQL para eliminar el cliente
            String query = "DELETE FROM CLIENTE WHERE IDCLIENTE = ?";

            // Crear una declaración preparada
            PreparedStatement pst = conexion.prepareStatement(query);

            // Asignar el ID del cliente a la consulta
            pst.setInt(1, idCliente);

            // Ejecutar la consulta
            int rowsAffected = pst.executeUpdate();

            // Verificar si se eliminó el cliente
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, 
                    "Cliente eliminado correctamente.", 
                    "Éxito", 
                    JOptionPane.INFORMATION_MESSAGE);

                // Limpiar el campo de texto después de la eliminación
                txtIdCliente.setText("");
                txtnombre.setText("");
                txtapellido.setText("");
                txtedad.setText("");
            
            } else {
                JOptionPane.showMessageDialog(this, 
                    "No se encontró un cliente con el ID especificado.", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, 
                "Error al eliminar el cliente: " + ex.getMessage(), 
                "Error de BD", 
                JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, 
                "El ID del cliente debe ser un número válido.", 
                "Error de validación", 
                JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed


