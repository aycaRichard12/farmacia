        // Consulta SQL para actualizar el cliente
        String query = "UPDATE CLIENTE SET NOMBRE_CLIENTE = ?, APELLIDO_CLIENTE = ?, EDAD = ? WHERE IDCLIENTE = ?";

        PreparedStatement pst = conexion.prepareStatement(query);
